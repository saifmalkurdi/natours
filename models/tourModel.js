tourSchema.pre(/^find/, function(next) {
  this.find({ secretTour: { $ne: true } });

  this.start = Date.now();
  next();
});

tourSchema.pre(/^find/, function(next) {
  this.populate({
    path: 'guides',
    select: '-__v -passwordChangedAt'
  });

  next();
});

tourSchema.post(/^find/, function(docs, next) {
  console.log(`Query took ${Date.now() - this.start} milliseconds!`);
  next();
});

// AGGREGATION MIDDLEWARE
//'there is some edit to make geoNear works'
tourSchema.pre('aggregate', function(next) {
  //  this point to the aggregation pipeline
  const pipeline = this.pipeline();
  // Find if $geoNear exists
  const geoNearIndex = pipeline.findIndex(stage => stage.$geoNear);
  if (geoNearIndex !== -1) {
    // Extract $geoNear stage
    const [geoNearStage] = pipeline.splice(geoNearIndex, 1);
    // Ensure $geoNear is the first stage
    pipeline.unshift(geoNearStage);
  }

  // Add $match stage only if $geoNear is not the first stage already
  if (!pipeline[0].$geoNear) {
    pipeline.unshift({ $match: { secretTour: { $ne: true } } });
  }

  next();
});

const Tour = mongoose.model('Tour', tourSchema);

module.exports = Tour;
